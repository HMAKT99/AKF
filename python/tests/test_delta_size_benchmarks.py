"""Tests for Delta 8: Size benchmarks — proving AKF is lightweight."""

import json
import os
import tempfile

import akf
from akf.models import Claim


class TestSizeBenchmarks:
    def test_minimal_file_under_200_bytes(self):
        """Minimal AKF: version + 1 claim should be tiny."""
        unit = akf.create("test claim", confidence=0.9)
        path = os.path.join(tempfile.gettempdir(), "min.akf")
        unit.save(path, compact=True)
        size = os.path.getsize(path)
        assert size < 300, f"Minimal file is {size} bytes, expected < 300"

    def test_5_claim_file_under_2kb(self):
        """5 claims with sources should be under 2KB."""
        claims = [
            {"content": f"Claim number {i} with moderate detail", "confidence": 0.8,
             "source": f"Source {i}"}
            for i in range(5)
        ]
        unit = akf.create_multi(claims)
        path = os.path.join(tempfile.gettempdir(), "five.akf")
        unit.save(path, compact=True)
        size = os.path.getsize(path)
        assert size < 2048, f"5-claim file is {size} bytes, expected < 2KB"

    def test_compact_smaller_than_descriptive(self):
        """Compact mode should produce smaller output than descriptive."""
        claims = [
            {"content": f"Detailed claim {i} about financial metrics", "confidence": 0.8 + i * 0.02,
             "source": f"Report {i}", "authority_tier": 2, "verified": True}
            for i in range(10)
        ]
        unit = akf.create_multi(claims)
        desc_path = os.path.join(tempfile.gettempdir(), "desc.akf")
        compact_path = os.path.join(tempfile.gettempdir(), "compact.akf")
        unit.save(desc_path, compact=False)
        unit.save(compact_path, compact=True)
        desc_size = os.path.getsize(desc_path)
        compact_size = os.path.getsize(compact_path)
        assert compact_size < desc_size

    def test_token_count_vs_prose(self):
        """AKF should use fewer tokens than equivalent prose."""
        prose = """
        According to the SEC 10-Q filing, Q3 revenue was $4.2 billion, representing
        a 12% increase year over year. This figure has been verified by Jane Doe from
        the finance team. The data was generated by GPT-4o with a temperature of 0.3
        and has a confidence level of 98%. The classification is confidential and this
        analysis was created on March 4, 2026.
        """
        prose_tokens = len(prose) / 4

        unit = akf.create("Q3 revenue $4.2B, up 12% YoY", confidence=0.98,
                          source="SEC 10-Q", verified=True)
        path = os.path.join(tempfile.gettempdir(), "token_test.akf")
        unit.save(path, compact=True)
        with open(path) as f:
            akf_text = f.read()
        akf_tokens = len(akf_text) / 4
        assert akf_tokens < prose_tokens


class TestPublicAPI:
    def test_public_api_complete(self):
        """Every model and function used in the demo must be importable."""
        required = [
            "create", "load", "save", "derive", "Claim", "Origin",
            "Review", "Evidence", "ReasoningChain", "GenerationParams",
            "is_expired", "freshness_status", "full_report",
        ]
        for name in required:
            assert hasattr(akf, name), f"akf.{name} is not exported"
